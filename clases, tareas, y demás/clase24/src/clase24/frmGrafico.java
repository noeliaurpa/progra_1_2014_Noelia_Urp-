    @Override
    public void paint(Graphics g) {
        super.paint(g);
        if (bandera == true) {
            String s1 = txtPartido1.getText();
            String s2 = txtPartido2.getText();
            String s3 = txtPartido3.getText();
            String s4 = txtPartido4.getText();
            String s5 = txtPartido5.getText();
            String s6 = txtPartido6.getText();
            int v1 = Integer.parseInt(s1);
            int v2 = Integer.parseInt(s2);
            int v3 = Integer.parseInt(s3);
            int v4 = Integer.parseInt(s4);
            int v5 = Integer.parseInt(s5);
            int v6 = Integer.parseInt(s6);
            int suma = v1 + v2 + v3 + v4 + v5 + v6;
            int grado1 = v1 * 360 / suma;
            int grado2 = v2 * 360 / suma;
            int grado3 = v3 * 360 / suma;
            int grado4 = v4 * 360 / suma;
            int grado5 = v5 * 360 / suma;
            int grado6 = v6 * 360 / suma;

            g.setColor(new Color(255, 0, 0));
            // el fillArc hace el circulo como estad√≠stica
            g.fillArc(50, 250, 200, 200, 0, grado1);
            g.fillRect(370, 250, 20, 20);
            g.drawString("Partido 1", 400, 270);

            g.setColor(new Color(0, 128, 0));
            g.fillArc(50, 250, 200, 200, grado1, grado2);
            g.fillRect(370, 280, 20, 20);
            g.drawString("Partido 2", 400, 300);

            g.setColor(new Color(0, 0, 225));
            g.fillArc(50, 250, 200, 200, grado1 + grado2, grado3);
            g.fillRect(370, 310, 20, 20);
            g.drawString("Partido 3", 400, 330);

            g.setColor(new Color(179, 64, 202));
            g.fillArc(50, 250, 200, 200, grado1 + grado2 + grado3, grado4);
            g.fillRect(370, 340, 20, 20);
            g.drawString("Partido 4", 400, 360);

            g.setColor(new Color(79, 123, 199));
            g.fillArc(50, 250, 200, 200, grado1 + grado2 + grado3 + grado4, grado5);
            g.fillRect(370, 370, 20, 20);
            g.drawString("Partido 5", 400, 390);

            g.setColor(new Color(242, 90, 232));
            g.fillArc(50, 250, 200, 200, grado1 + grado2 + grado3 + grado4 + grado5, grado6);
            g.fillRect(370, 400, 20, 20);
            g.drawString("Partido 6", 400, 420);
        }
    }

